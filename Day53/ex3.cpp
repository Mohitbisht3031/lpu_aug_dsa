vector<vector<int>> make_matrix(){
    int n,e;
    cout<<"Enter no. of node and edges in the graph :";
    cin>>n>>e;
    cout<<endl;
    vector<vector<int>>g(vector<vector<int>>(n,vector<int>(n,0)));
    for(int i = 0;i<e;i++){
        int a,b;
        cout<<"give nodes with direct edge";
        cin>>a,b;
        if(a < n && b < n){
            g[a][b] = 1;
            g[b][a] = 1;
        }
    }

    return g;
}

vector<vector<int>> make_list(){
    int n,e;
    cout<<"Enter no. of node and edges in the graph :";
    cin>>n>>e;
    cout<<endl;
    vector<vector<int>>g(n);
    for(int i = 0;i<e;i++){
        int a,b;
        cout<<"give nodes with direct edge";
        cin>>a,b;
        if(a < n && b < n){
            g[a].push_back(b);
            g[b].push_back(a);
        }
    }

    return g;
}

void dfs(vector<vector<int>>&g,vector<int>&vis,int src){
    vis[src] = 1;
    for(int nbr : g[src]){
        if(!vis[nbr])dfs(g,vis,nbr);
    }
    return;
}

int connectedComp(vector<vector<int>>&g){
    int c = 0;
    vector<int>vis(g.size(),0);
    for(int i = 0;i<vis.size();i++){
        if(!vis[i]){
            c++;
            dfs(g,vis,i);    
        }
    }
    return c;
}

